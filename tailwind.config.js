// Create a function that can be used to merge configs when used as a preset
const mergeConfigs = (customConfig = {}) => {
  return {
    ...defaultConfig,
    ...customConfig,
    theme: {
      ...defaultConfig.theme,
      ...(customConfig.theme || {}),
      extend: {
        ...defaultConfig.theme.extend,
        ...(customConfig.theme?.extend || {}),
      },
    },
    plugins: [
      ...defaultConfig.plugins,
      ...(customConfig.plugins || []),
    ],
  };
};

